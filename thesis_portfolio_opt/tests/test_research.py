# LEAKAGE CHECK
# ============================================================================

class TestNoFutureLeakage:
    """Verify that the experiment code excludes return columns from predictors."""

    def test_no_future_leakage(self):
        """Feature columns used as predictors must not contain '_ret_'.

        The experiment code's ``select_features`` function should filter
        out return columns to prevent look-ahead bias.  We verify this by
        importing the function and checking its output on the actual
        feature set.
        """
        features_path = PROCESSED_DIR / "features.csv"
        if not features_path.exists():
            pytest.skip("features.csv not available")

        # Read feature columns
        df = pd.read_csv(features_path, index_col=0, nrows=1)

        # Import the experiment's select_features function
        import importlib.util
        experiment_path = PROJECT_ROOT / "research" / "experiment.py"
        if not experiment_path.exists():
            pytest.skip("research/experiment.py not available")

        spec = importlib.util.spec_from_file_location(
            "experiment_module", str(experiment_path)
        )
        mod = importlib.util.module_from_spec(spec)

        # We only need select_features, so suppress side effects
        import sys
        old_argv = sys.argv
        sys.argv = ["test"]
        try:
            spec.loader.exec_module(mod)
        except SystemExit:
            pass
        finally:
            sys.argv = old_argv

        selected = mod.select_features(df)
        leaky = [c for c in selected if "_ret_" in c]
        assert len(leaky) == 0, (
            f"Leakage detected: {len(leaky)} return columns in predictors: "
            f"{leaky[:10]}"
        )


