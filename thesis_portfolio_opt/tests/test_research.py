class TestBenchmarks:
    """Ensure benchmark Sharpe ratios are in reasonable ranges."""

    @pytest.fixture
    def prices(self):
        path = RAW_DIR / "prices.csv"
        if not path.exists():
            pytest.skip("prices.csv not available")
        return pd.read_csv(path, index_col=0, parse_dates=True)

    def test_benchmark_sharpe_reasonable(self, prices):
        """SPY Sharpe should be between 0 and 2; Equal Weight between -1 and 2."""
        # --- SPY ---
        spy_ret = prices["SPY"].pct_change().dropna()
        spy_ann_ret = spy_ret.mean() * 252
        spy_ann_vol = spy_ret.std() * np.sqrt(252)
        spy_sharpe = spy_ann_ret / spy_ann_vol if spy_ann_vol > 0 else 0.0
        assert 0 < spy_sharpe < 2, f"SPY Sharpe out of range: {spy_sharpe:.4f}"

        # --- Equal Weight ---
        ew_ret = prices.pct_change().dropna().mean(axis=1)
        ew_ann_ret = ew_ret.mean() * 252
        ew_ann_vol = ew_ret.std() * np.sqrt(252)
        ew_sharpe = ew_ann_ret / ew_ann_vol if ew_ann_vol > 0 else 0.0
        assert -1 < ew_sharpe < 2, f"Equal Weight Sharpe out of range: {ew_sharpe:.4f}"


